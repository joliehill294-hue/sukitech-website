/* ===== State ===== */
let currentLang = localStorage.getItem('sukitech-lang') || 'zh';

/* ===== Apply Language ===== */
function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('sukitech-lang', lang);
  const d = i18n[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (d[key] !== undefined) el.innerHTML = d[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (d[key] !== undefined) el.placeholder = d[key];
  });
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'es';
  document.getElementById('langToggle').textContent = lang === 'zh' ? 'ES / 中文' : '中文 / ES';
}

/* ===== Language Toggle ===== */
document.getElementById('langToggle').addEventListener('click', () => {
  setLang(currentLang === 'zh' ? 'es' : 'zh');
});

/* ===== Sticky Nav ===== */
const nav = document.getElementById('mainNav');
const hero = document.querySelector('.hero');

let observer;
function setupScrollObserver() {
  const opts = { threshold: [0, 1] };
  observer = new IntersectionObserver((entries) => {
    const entry = entries[0];
    if (entry.boundingClientRect.bottom < 0 || entry.intersectionRatio < 0.1) {
      nav.classList.add('scrolled');
    } else {
      nav.classList.remove('scrolled');
    }
  }, opts);
  if (hero) observer.observe(hero);
}

/* ===== Active Section Highlight ===== */
function setupSectionObserver() {
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('.nav-link[data-section]');
  const secObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        navLinks.forEach(link => {
          link.classList.toggle('active', link.dataset.section === entry.target.id);
        });
      }
    });
  }, { rootMargin: '-40% 0px -55% 0px' });

  sections.forEach(s => secObserver.observe(s));
}

/* ===== Scroll Animations ===== */
function setupAnimations() {
  const animEls = document.querySelectorAll('.about-card, .case-card, .venue-card, .mat-card, .plan-card, .news-card, .contact-box');
  animEls.forEach(el => el.classList.add('animate'));

  const animObs = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        animObs.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15, rootMargin: '0px 0px -30px 0px' });

  animEls.forEach(el => animObs.observe(el));
}

